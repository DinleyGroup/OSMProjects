package indoorFeatures;

public class Passage extends UnitSpace {
boolean isCorridor;
    
    public Passage(String buildingPart, long height, boolean isIndoor, int level, String name, boolean isCorridor) {
	super(buildingPart, height, isIndoor, level, name);
	this.isCorridor = isCorridor;
    }
    
    public boolean isCorridor() {
	return isCorridor;
    }
    
    public void setCorridor(boolean corridor) {
	isCorridor = corridor;
    }
}
